package cl.contraloria.sicogen.model;

public class PlanCuentaTitulo {
    private String idCtaContable;
    private String codTituloCta;
    private String desTituloCta;

    public String getIdCtaContable() {
        return idCtaContable;
    }

    public void setIdCtaContable(String idCtaContable) {
        this.idCtaContable = idCtaContable;
    }

    public String getCodTituloCta() {
        return codTituloCta;
    }

    public void setCodTituloCta(String codTituloCta) {
        this.codTituloCta = codTituloCta;
    }

    public String getDesTituloCta() {
        return desTituloCta;
    }

    public void setDesTituloCta(String desTituloCta) {
        this.desTituloCta = desTituloCta;
    }
}
