package cl.contraloria.sicogen.model;

public class Bitacora {
    private String fchaCreacion;
    private String userCreacion;
    private String fchaEdicion;
    private String userEdicion;
    private String fchaDesactivacion;
    private String userDesactivacion;

    public String getFchaCreacion() {
        return fchaCreacion;
    }
    public void setFchaCreacion(String fchaCreacion) {
        this.fchaCreacion = fchaCreacion;
    }
    public String getUserCreacion() {
        return userCreacion;
    }
    public void setUserCreacion(String userCreacion) {
        this.userCreacion = userCreacion;
    }
    public String getFchaEdicion() {
        return fchaEdicion;
    }
    public void setFchaEdicion(String fchaEdicion) {
        this.fchaEdicion = fchaEdicion;
    }
    public String getUserEdicion() {
        return userEdicion;
    }
    public void setUserEdicion(String userEdicion) {
        this.userEdicion = userEdicion;
    }
    public String getFchaDesactivacion() {
        return fchaDesactivacion;
    }
    public void setFchaDesactivacion(String fchaDesactivacion) {
        this.fchaDesactivacion = fchaDesactivacion;
    }
    public String getUserDesactivacion() {
        return userDesactivacion;
    }
    public void setUserDesactivacion(String userDesactivacion) {
        this.userDesactivacion = userDesactivacion;
    }
}
