package cl.contraloria.sicogen.model;

public class Mantenedores {
    private String mantId;
    private String mantCod;
    private String mantNombre;
    private String mantAccion;
    private String mantVigencia;
    private String mantUsuario;
    private String mantfecha;

    public String getMantId() {
        return mantId;
    }

    public void setMantId(String mantId) {
        this.mantId = mantId;
    }

    public String getMantCod() {
        return mantCod;
    }

    public void setMantCod(String mantCod) {
        this.mantCod = mantCod;
    }

    public String getMantNombre() {
        return mantNombre;
    }

    public void setMantNombre(String mantNombre) {
        this.mantNombre = mantNombre;
    }

    public String getMantAccion() {
        return mantAccion;
    }

    public void setMantAccion(String mantAccion) {
        this.mantAccion = mantAccion;
    }

    public String getMantVigencia() {
        return mantVigencia;
    }

    public void setMantVigencia(String mantVigencia) {
        this.mantVigencia = mantVigencia;
    }

    public String getMantUsuario() {
        return mantUsuario;
    }

    public void setMantUsuario(String mantUsuario) {
        this.mantUsuario = mantUsuario;
    }

    public String getMantfecha() {
        return mantfecha;
    }

    public void setMantfecha(String mantfecha) {
        this.mantfecha = mantfecha;
    }
}
